lista=[1,2,3,4,5]
print(lista[2:5])
print(lista[:5])
print(lista[3:])
print(lista[3:5:2]) #the first two values are the start and the end values the third value is the increment value

print(lista[::2])#printing everything with an increment of two
print(lista[::-1]) #reversing a list

#the same concept can be applied with the tuples also